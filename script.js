const fallback = translations.en;
const select = document.querySelector("#languageSelect");

function applyLanguage(lang) {
  const dictionary = { ...fallback, ...(translations[lang] || {}) };
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    element.textContent = dictionary[key] || fallback[key] || element.textContent;
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const key = element.dataset.i18nPlaceholder;
    element.placeholder = dictionary[key] || fallback[key] || element.placeholder;
  });
  if (select) {
    select.value = lang;
  }
  localStorage.setItem("wilson-language", lang);
}

const savedLanguage = localStorage.getItem("wilson-language") || "en";
applyLanguage(savedLanguage);

if (select) {
  select.addEventListener("change", (event) => applyLanguage(event.target.value));
}

const chatToggle = document.querySelector("#chatToggle");
const chatPanel = document.querySelector("#chatPanel");
const chatClose = document.querySelector("#chatClose");
const chatForm = document.querySelector("#chatForm");
const chatInput = document.querySelector("#chatInput");
const chatMessages = document.querySelector("#chatMessages");

function currentDictionary() {
  const lang = localStorage.getItem("wilson-language") || "en";
  return { ...fallback, ...(translations[lang] || {}) };
}

function addMessage(text, type) {
  const message = document.createElement("p");
  message.className = type;
  message.textContent = text;
  chatMessages.appendChild(message);
  chatMessages.scrollTop = chatMessages.scrollHeight;
}

if (chatToggle && chatPanel) {
  chatToggle.addEventListener("click", () => {
    chatPanel.hidden = !chatPanel.hidden;
    if (!chatPanel.hidden) {
      chatInput.focus();
    }
  });
}

if (chatClose) {
  chatClose.addEventListener("click", () => {
    chatPanel.hidden = true;
  });
}

if (chatForm) {
  chatForm.addEventListener("submit", (event) => {
    event.preventDefault();
    const text = chatInput.value.trim();
    if (!text) return;
    addMessage(text, "user");
    chatInput.value = "";
    window.setTimeout(() => addMessage(currentDictionary().chatReply, "bot"), 450);
  });
}

window.googleTranslateElementInit = function googleTranslateElementInit() {
  if (!window.google || !google.translate) return;
  new google.translate.TranslateElement({
    pageLanguage: "en",
    layout: google.translate.TranslateElement.InlineLayout.SIMPLE,
    autoDisplay: false
  }, "google_translate_element");
};
